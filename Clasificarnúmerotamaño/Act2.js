let numero = 8;

if (numero >= 0) {
    if (numero > 10) {
        console.log("El número es positivo y mayor que 10");
    } else {
        console.log("El número es positivo y menor o igual a 10");
    }
} else {
    if (numero < -10) {
        console.log("El número es negativo y menor que -10");
    } else {
        console.log("El número es negativo y mayor o igual a -10");
    }
}
